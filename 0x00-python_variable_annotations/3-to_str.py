#!/usr/bin/env python3
"""
3-to_str.py

This module contains the function to_str which returns the string
representation of a given floating-point number.

Author: Your Name
Date: Date of completion
"""

def to_str(n: float) -> str:
    """
    Returns the string representation of a floating-point number.

    Args:
        n (float): The floating-point number.

    Returns:
        str: The string representation of the number n.
    """
    return str(n)
